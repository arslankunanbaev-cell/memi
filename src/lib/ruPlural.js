/**
 * Russian plural forms.
 *
 * pluralRu(1, 'момент', 'момента', 'моментов') → 'момент'
 * pluralRu(3, 'момент', 'момента', 'моментов') → 'момента'
 * pluralRu(11,'момент', 'момента', 'моментов') → 'моментов'
 * pluralRu(21,'момент', 'момента', 'моментов') → 'момент'
 */
export function pluralRu(n, one, few, many) {
  const mod100 = Math.abs(n) % 100
  const mod10  = Math.abs(n) % 10
  if (mod100 >= 11 && mod100 <= 14) return many
  if (mod10 === 1)                  return one
  if (mod10 >= 2 && mod10 <= 4)    return few
  return many
}

/** Готовые склонения для часто используемых слов */
export const plural = {
  момент:  (n) => pluralRu(n, 'момент',  'момента',  'моментов'),
  месяц:   (n) => pluralRu(n, 'месяц',   'месяца',   'месяцев'),
  человек: (n) => pluralRu(n, 'человек', 'человека', 'человек'),
}

